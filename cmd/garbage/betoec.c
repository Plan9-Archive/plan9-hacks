#include <u.h>
#include <libc.h>
#include <mp.h>
#include <libsec.h>

uchar pubkey[] = { 0x04, 0x89, 0xe7, 0x24, 0x5b, 0x6e, 0x73, 0x47, 0x36, 0xa4, 0xcb, 0xe4, 0x68, 0xf8, 0x95, 0x9b, 0x81, 0x6f, 0xce, 0x51, 0x75, 0x3b, 0xdd, 0xe8, 0x21, 0xb5, 0xc3, 0x00, 0x68, 0x51, 0x22, 0x86, 0x59, 0xbc, 0x79, 0xb4, 0x69, 0x7d, 0x08, 0xf1, 0x0f, 0xf9, 0x17, 0x04, 0xb8, 0xdd, 0xc5, 0x56, 0xea, 0xa7, 0x9c, 0x2f, 0x8b, 0x07, 0xd0, 0xcb, 0x50, 0xc7, 0xe4, 0x02, 0x44, 0xa1, 0xf4, 0xf9, 0x0f };

void
main(void)
{
	fmtinstall('B', mpfmt);

	ECdomain *dom = ecnamedcurve(Secp256r1);

	if(dom == nil)
		sysfatal("named curve failed");

	ECpoint *pubec = betoec(dom, pubkey, sizeof(pubkey), nil);

	if(pubec == nil)
		sysfatal("betoec failed");

	print("x %d %B\n", (mpsignif(pubec->x)+7)>> 3, pubec->x);
	print("y %d %B\n", (mpsignif(pubec->y)+7) >> 3, pubec->y);

	exits(nil);
}

